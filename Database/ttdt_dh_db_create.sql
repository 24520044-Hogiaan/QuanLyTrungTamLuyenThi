    CONSTRAINT fk_thamgiabkt_bkt FOREIGN KEY (MABAIKIEMTRA)
        REFERENCES BAIKIEMTRA(MABAIKIEMTRA),
    CONSTRAINT chk_tbkt_diemso CHECK (DIEMSO IS NULL OR DIEMSO >= 0),
    CONSTRAINT chk_tbkt_lanthi CHECK (LANTHI >= 1)
